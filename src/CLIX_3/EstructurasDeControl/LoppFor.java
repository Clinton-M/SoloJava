package CLIX_3.EstructurasDeControl;

public class LoppFor {
    public static void main(String[] args) {


        for (int i = 0; i < 5; i++) {
            System.out.println("Soy " + i);
        }

        for (int j = 0; j < 10; j++) {
            System.out.println(" " + j);
        }


        for (int k = 0; k < 10; k++) {
            System.out.println(" " + k);

        }
        System.out.println("Estoy fuera");
    }
}


